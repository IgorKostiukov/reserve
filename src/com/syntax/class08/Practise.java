package com.syntax.class08;



public class Practise {
	public static void main(String[] args) {
		for (int i = 1; i <= 100; i++) {
			System.out.print(i + " ");

		}
		System.out.println();
		System.out.println("------------------------------");

		for (int i = 100; i >= 1; i--) {
			System.out.print(i + " ");
		}
		System.out.println();
		System.out.println("------------------------------");
		for (int k = 20; k >= 2; k -= 2) {

			System.out.print(k + " ");
		}
		System.out.println();
		System.out.println("------------------------------");
		for (int t = 20; t >= 1; t--) {
			if (t % 2 == 0) {
				System.out.print(t + " ");
			}
		}
		System.out.println();
		System.out.println("------------------------------");
		for (int h = 21; h <= 50; h += 2) {
			System.out.print(h + " ");

		}
		System.out.println();
		System.out.println("------------------------------");
		for (int y = 20; y <= 50; y++) {
			if (y % 2 == 1) {
				System.out.print(y + " ");
			}
		}
		
		
			
		
		
		
		
	}
}
