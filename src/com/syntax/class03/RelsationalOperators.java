package com.syntax.class03;

public class RelsationalOperators {
	public static void main(String[] args) {
		int num1=20;
		int num2=22;
		System.out.println(num1>num2); //false
		System.out.println(num1<num2); //true
		System.out.println(num1!=num2); //true
		System.out.println(num1==num2); //false
	}

}
